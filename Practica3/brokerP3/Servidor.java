

public class Servidor {
    String nombreServidor;
    String IP;

    public Servidor(String nombreServidor, String IP) {
        this.nombreServidor = nombreServidor;
        this.IP = IP;
    }

    // Función que el String de la IP del servidor
    public String getIP() {
        return IP;
    }

    // Función que el String del nombre del servidor
    public String getNombre() {
        return nombreServidor;
    }
}




 
